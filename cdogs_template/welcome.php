<p>Attention all grad students and post-docs:</p>

<p> We invite you to give a presentation at the Seventh Conference of
Dalhousie Oceanography Grad Students (C-DOGS), which will take place
on Friday March 13th, 2009. This is an opportunity for you to share
your research and learn about the research of your peers in a friendly
environment. The day will be structured in a conference style, with 15
minutes per speaker (including set-up and questions). Although only
graduate students and post-docs are invited to present, we will be
inviting others to attend, including the faculty and staff of the
Oceanography Department, and scientists at the Bedford Institute of
Oceanography.  We encourage everyone to give a presentation. Are you
wondering what to talk to about?  In short: whatever you want. Are you
a new student? You could talk about your previous work, a class
project, or ideas that may end up in your thesis proposal. Are you a
world expert on photosystem II or mixed-layer turbulence? We'd also
like to hear about it...just make sure we can understand.  </p>
